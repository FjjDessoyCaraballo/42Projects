#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*node;

	node = (t_list *)malloc(sizeof(content));
	if (!node)
		return (NULL);
	node->content = content;
	node->next = NULL;
	return (node);
}
